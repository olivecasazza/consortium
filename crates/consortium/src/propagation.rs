        // queued before setup: no pickup yet
        ch.shell("node[1-2]", b"echo hi", 1);
        assert!(ch.pickup_events.is_empty());

        // ACK dequeues and sends the CTL: pickup fires now
        ch.recv(Message::ack(0)).unwrap();
